// First C++ Program

#include <iostream> 

int main() {
    std::cout << "Hello World!";
    return 0;
}

/*
# include <iostream>

        #include is a preprocessor directive used to include files in our program
        The above code is including the contents of the iostream file

        This allows us to use `cout` in the program to print output to the screen

int main() {...}

        A vaild C++ program must have the main() function. 

std::cout << "Hello World!";

        std:cout prints the content inside the quotation marks. 
        It must be followed by << followed by the format string
        "Hello World!" is the format string and `;` indicates the end of a statement.

return 0;

        This is the "Exit status" of the program
*/